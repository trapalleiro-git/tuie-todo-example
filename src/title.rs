//! Big-glyph text title widget.

use tuie::{field, prelude::*};

pub(crate) struct Title {
    layout: Layout,
    text: String,
    compact: bool,
    rows: Vec<String>,
    width: u16,
    height: u16,
}

impl Title {
    fn rebuild(&mut self) {
        let char_count = self.text.chars().count();
        self.width = (char_count * 3) as u16;
        let mut grid: Vec<Vec<&'static str>> = vec![Vec::with_capacity(char_count); 6];
        for c in self.text.chars() {
            let g = large_glyph(c).unwrap_or(["   ", "   ", "   ", "   ", "   ", "   "]);
            for i in 0..6 {
                grid[i].push(g[i]);
            }
        }
        let mut start = 0usize;
        let mut end = 6usize;
        if self.compact {
            if grid[0].iter().all(|s| s.chars().all(|c| c == ' ')) {
                start = 1;
            }
            if grid[5].iter().all(|s| s.chars().all(|c| c == ' ')) {
                end = 5;
            }
        }
        self.height = (end - start) as u16;
        self.rows = (start..end).map(|i| grid[i].concat()).collect();
    }
}

impl Widget for Title {
    fn get_layout(&self) -> &Layout {
        &self.layout
    }

    fn get_layout_mut(&mut self) -> &mut Layout {
        &mut self.layout
    }

    fn get_name(&self) -> &'static str {
        "Title"
    }

    fn measure_constraints(&mut self) -> Constraints {
        let size = Vec2::new(self.width, self.height);
        Constraints {
            min_size: size,
            max_size: size,
            preferred_size: size,
        }
    }

    fn render(&self, mut ctx: RenderContext) {
        ctx.set_style(self.layout.style);
        for (i, row) in self.rows.iter().enumerate() {
            ctx.move_to((0, i as i32).into());
            ctx.write(row);
        }
    }
}

impl Title {
    pub(crate) fn new(text: &str) -> Box<Self> {
        let mut title = Box::new(Self {
            layout: Layout::new(),
            text: text.to_string(),
            compact: false,
            rows: Vec::new(),
            width: 0,
            height: 0,
        });
        title.rebuild();
        title
    }

    pub(crate) fn set_text(&mut self, text: &str) {
        if self.text == text {
            return;
        }
        self.text = text.to_string();
        self.rebuild_and_dirty();
    }

    fn rebuild_and_dirty(&mut self) {
        self.rebuild();
        self.dirty_layout();
    }

    field!(compact: bool; rebuild_and_dirty);
}

